def summary_lines(going: List[str], maybe: List[str], nope: List[str], target: int) -> str:
    parts: List[str] = [SUMMARY_TITLE]
    parts.append(f"–ò–¥—É ({len(going)}/{target}): " + (", ".join(going) if going else "‚Äî"))
